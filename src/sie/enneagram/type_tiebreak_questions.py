"""Tie-breaker type questions for borderline scores within a center."""

from __future__ import annotations

from sie.enneagram.questions import Question, _opts
from sie.enneagram.types import CENTER_TYPES, Center

TYPE_TIEBREAK_OPTION_WEIGHT = 3.0

_TYPE_ORDER_BY_CENTER: dict[Center, tuple[int, ...]] = {
    Center.BODY: (8, 9, 1),
    Center.HEART: (2, 3, 4),
    Center.HEAD: (5, 6, 7),
}


def normalize_type_pair(
    center: Center,
    a: int,
    b: int,
) -> tuple[int, int]:
    """Return pair in canonical order for the center (8→9→1, 2→3→4, 5→6→7)."""
    order = _TYPE_ORDER_BY_CENTER[center]
    if order.index(a) > order.index(b):
        return (b, a)
    return (a, b)


def _tt_q(
    pair_key: str,
    num: int,
    text: str,
    first: int,
    first_text: str,
    second: int,
    second_text: str,
) -> Question:
    return Question(
        id=f"tt_{pair_key}_{num:02d}",
        text=text,
        category="type_tiebreak",
        options=_opts(
            (first_text, {str(first): TYPE_TIEBREAK_OPTION_WEIGHT}),
            (second_text, {str(second): TYPE_TIEBREAK_OPTION_WEIGHT}),
        ),
    )


